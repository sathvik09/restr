        <h5 class="card-title">ROSE MILK</h5>
        <p class="card-text">
          Rose Milk is our special drink, that’s a bit expensive in those
          days. So if we want to have rose milk, we will spare money for two
          days and enjoy it on the third day. Its a wonderful feeling when we
          wait for something and finally get to enjoy it.
        </p>
        <p class="card-text">
          Ingredients: 1 1/2 cup Water 1 cup Sugar 2 cups Fresh Rose Petals 2
          drops Pink Food Color as required Milk
        </p>
        <p class="card-text">
          Method: Boil the water and rose petals for 10 minutes. Strain the
          petals after boiling. Add in sugar and boil until its thick like a
          syrup. At last add in the color and let it cool down completely.
          Store in clean glass bottles for up to 2 weeks. To make rose milk,
          add 2-3 tbsp of syrup to cold milk and serve!
        </p>
