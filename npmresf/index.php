        <p class="card-text">
          I was actually worried if I could find mozzarella cheese during this
          time. But I was lucky to find cheese and paneer at our near by
          store. If you couldn’t find one, feel free to use whatever other
          veggies that are available to you. I have also used homemade pizza
          sauce.
        </p>
        <p class="card-text">
          To make Pizza Sauce: Boil 2 tomato, 1 onion, 3 clove garlic, 3 whole
          red chilli in 1 cup water for 5 minutes. Once they are cooled,
          remove skin for tomato and grind it all together, discard the
          remaining water. Heat a pan, add oil, tomato paste and let it come
          to boil, add 1/2 tsp oregano and salt as required. Let it boil until
          thick for about 5 minutes. Pizza sauce is ready! Store in air tight
          container for up to a week. This recipe will yield 3/4 cup of sauce.
        </p>
